package ParcticeKG;
import java.util.*;

// MATHS FUNCTION IN JAVA
 
class rd_math{
    public static void main(String[] args) {
        System.out.println(Math.abs(-99));
        System.out.println(Math.max(99, 100));
        System.out.println(Math.min(99, 100));
        System.out.println(Math.pow(2, 3));
        System.out.println(Math.sqrt(25));
        System.out.println(Math.cbrt(27));
        System.out.println(Math.random()); // here random generate the value between 0 to 1 in decimals
        System.out.println(Math.random()*100); // here random generate the value between 0 to 1 in decimals
        System.out.println(Math.round(4.5));
    }
}
